    private void jButton22ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton22ActionPerformed
        SimpleDateFormat dateFormat = new SimpleDateFormat("dd-MM-yyyy");
        int getday,getmonth,getyear,setday = 0,setmonth,setyear,changeday,changemonth,changeyear;
        String month = "";
        
        getyear = Integer.parseInt(dateFormat.format(jTextField3.getDate()).substring(6,10));
        getmonth = Integer.parseInt(dateFormat.format(jTextField3.getDate()).substring(3,5));
        getday = Integer.parseInt(dateFormat.format(jTextField3.getDate()).substring(0,2));
        
        getday += getyear * 365 + getmonth * 30;
        
        changeyear = (Integer) jSpinner1.getValue();
        changemonth = (Integer) jSpinner2.getValue();
        changeday = (Integer) jSpinner3.getValue();
        
        changeday += changeyear*365 + changemonth*30;
        
        if(jRadioButton1.isSelected()){
            setday = getday + changeday;
        }else if(jRadioButton2.isSelected()){
            setday = getday - changeday;
        }
        setyear = setday/365;
        setday %= 365;
        setmonth = setday/30;
        setday %= 30;
        switch (setmonth) {
            case 1:
                month = "January";
                break;
            case 2:
                month = "February";
                break;
            case 3:
                month = "March";
                break;
            case 4:
                month = "April";
                break;
            case 5:
                month = "May";
                break;
            case 6:
                month = "June";
                break;
            case 7:
                month = "July";
                break;
            case 8:
                month = "August";
                break;
            case 9:
                month = "September";
                break;
            case 10:
                month = "October";
                break;
            case 11:
                month = "November";
                break;
            case 12:
                month = "December";
                break;
            default:
                break;
        }
        jTextField4.setText(setday+" "+month+", "+setyear);
    }//GEN-LAST:event_jButton22ActionPerformed

